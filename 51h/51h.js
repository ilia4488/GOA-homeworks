// Math.round() ამრგვალებს რიცხვს უახლოეს მთელამდე
console.log(Math.round(4.3))  // 4
console.log(Math.round(4.7))  // 5
console.log(Math.round(5.5))  // 6
console.log(Math.round(2.49)) // 2
console.log(Math.round(2.5))  // 3
// Math.abs() აბრუნებს რიცხვის აბსოლუტურ მნიშვნელობას (მოდულს)
console.log(Math.abs(-5))   // 5
console.log(Math.abs(10))   // 10
console.log(Math.abs(-3.2)) // 3.2
console.log(Math.abs(0))    // 0
console.log(Math.abs(-100)) // 100
// Math.floor() ამრგვალებს ქვემოთ (down)
console.log(Math.floor(4.9))  // 4
console.log(Math.floor(4.1))  // 4
console.log(Math.floor(5.99)) // 5
console.log(Math.floor(2.5))  // 2
console.log(Math.floor(-2.3)) // -3
// Math.ceil() ამრგვალებს ზემოთ (up)
console.log(Math.ceil(4.1))  // 5
console.log(Math.ceil(4.9))  // 5
console.log(Math.ceil(5.01)) // 6
console.log(Math.ceil(2.1))  // 3
console.log(Math.ceil(-2.3)) // -2
// Math.sign() აჩვენებს რიცხვის ნიშანს
console.log(Math.sign(10))   // 1
console.log(Math.sign(-10))  // -1
console.log(Math.sign(0))    // 0
console.log(Math.sign(3.5))  // 1
console.log(Math.sign(-0.5)) // -1
